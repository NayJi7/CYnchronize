EXEC DBMS_MVIEW.REFRESH('MV_SITE',                   'C');
EXEC DBMS_MVIEW.REFRESH('MV_LOCATION',               'C');
EXEC DBMS_MVIEW.REFRESH('MV_TYPE_MATERIEL',          'C');
EXEC DBMS_MVIEW.REFRESH('MV_CONSTRUCTEUR',           'C');
EXEC DBMS_MVIEW.REFRESH('MV_MODELE',                 'C');
EXEC DBMS_MVIEW.REFRESH('MV_TYPE_EQUIPEMENT_RESEAU', 'C');
EXEC DBMS_MVIEW.REFRESH('MV_GROUPE',                 'C');
EXEC DBMS_MVIEW.REFRESH('MV_PROFIL',                 'C');

-- Synchronisation des tables referentielles depuis les MV
-- Necessaire pour satisfaire les FK des tables operationnelles
ALTER TABLE SITE                   MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE LOCATION               MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE TYPE_MATERIEL          MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE CONSTRUCTEUR           MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE MODELE                 MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE TYPE_EQUIPEMENT_RESEAU MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE GROUPE                 MODIFY id GENERATED BY DEFAULT AS IDENTITY;
ALTER TABLE PROFIL                 MODIFY id GENERATED BY DEFAULT AS IDENTITY;

INSERT INTO SITE (id, code, nom, adresse)
    SELECT id, code, nom, adresse FROM MV_SITE m
    WHERE NOT EXISTS (SELECT 1 FROM SITE s WHERE s.id = m.id);

INSERT INTO LOCATION (id, site_id, batiment, etage, salle)
    SELECT id, site_id, batiment, etage, salle FROM MV_LOCATION m
    WHERE NOT EXISTS (SELECT 1 FROM LOCATION l WHERE l.id = m.id);

INSERT INTO TYPE_MATERIEL (id, libelle)
    SELECT id, libelle FROM MV_TYPE_MATERIEL m
    WHERE NOT EXISTS (SELECT 1 FROM TYPE_MATERIEL t WHERE t.id = m.id);

INSERT INTO CONSTRUCTEUR (id, nom)
    SELECT id, nom FROM MV_CONSTRUCTEUR m
    WHERE NOT EXISTS (SELECT 1 FROM CONSTRUCTEUR c WHERE c.id = m.id);

INSERT INTO MODELE (id, constructeur_id, type_materiel_id, reference)
    SELECT id, constructeur_id, type_materiel_id, reference FROM MV_MODELE m
    WHERE NOT EXISTS (SELECT 1 FROM MODELE mo WHERE mo.id = m.id);

INSERT INTO TYPE_EQUIPEMENT_RESEAU (id, libelle)
    SELECT id, libelle FROM MV_TYPE_EQUIPEMENT_RESEAU m
    WHERE NOT EXISTS (SELECT 1 FROM TYPE_EQUIPEMENT_RESEAU t WHERE t.id = m.id);

INSERT INTO GROUPE (id, nom, description)
    SELECT id, nom, description FROM MV_GROUPE m
    WHERE NOT EXISTS (SELECT 1 FROM GROUPE g WHERE g.id = m.id);

INSERT INTO PROFIL (id, nom, description)
    SELECT id, nom, description FROM MV_PROFIL m
    WHERE NOT EXISTS (SELECT 1 FROM PROFIL p WHERE p.id = m.id);

COMMIT;

SELECT 'SITE'          AS tbl, COUNT(*) FROM SITE
UNION ALL SELECT 'LOCATION',      COUNT(*) FROM LOCATION
UNION ALL SELECT 'TYPE_MATERIEL', COUNT(*) FROM TYPE_MATERIEL
UNION ALL SELECT 'CONSTRUCTEUR',  COUNT(*) FROM CONSTRUCTEUR
UNION ALL SELECT 'MODELE',        COUNT(*) FROM MODELE;

EXIT;
